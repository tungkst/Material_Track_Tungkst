export type MaterialStatus = 'Approved' | 'Approved as Noted' | 'Revise & Resubmit' | 'Rejected' | 'Pending' | 'Omitted' | 'No Sample Required';
export type ProcurementStatus = 'Not Ordered' | 'Ordered' | 'In Transit' | 'Received';
export type MaterialType = 'Fitout' | 'Architecture' | 'Furniture' | 'MEP' | 'BasicFinish' | 'Decor' | 'Equipment';

export interface SubmissionLogEntry {
  id: string;
  submissionDate: string;
  status: MaterialStatus;
  notes: string;
}

